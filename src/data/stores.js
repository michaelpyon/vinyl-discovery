// Get genre summary for a region
export function getRegionGenres(stores) {
  const genreCount = {}
  stores.forEach((store) => {
    store.genres.forEach((g) => {
      genreCount[g] = (genreCount[g] || 0) + 1
    })
  })
  return Object.entries(genreCount)
    .sort((a, b) => b[1] - a[1])
    .map(([genre]) => genre)
}

// Get all unique genres from all stores
export function getAllGenres() {
  const genres = new Set()
  stores.forEach((store) => {
    store.genres.forEach((g) => genres.add(g))
  })
  return Array.from(genres).sort()
}

// Get unique cities with their coordinates
export function getCities() {
  const cityMap = new Map()
  stores.forEach((store) => {
    const key = `${store.city}, ${store.country}`
    if (!cityMap.has(key)) {
      cityMap.set(key, {
        label: store.city,
        country: store.country,
        lat: store.lat,
        lng: store.lng,
      })
    }
  })
  return Array.from(cityMap.values())
}

